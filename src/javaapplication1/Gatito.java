package javaapplication1;

/**
 *
 * @author YPAREDES
 */
public class Gatito {
    
    public int edad;
    public String color;
    public int tamaño;
    
    public Gatito(int edad, String color, int tamaño) {
        this.edad = edad;
        this.color = color;
        this.tamaño = tamaño;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getTamaño() {
        return tamaño;
    }

    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }
    
}
